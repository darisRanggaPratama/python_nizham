import turtle

# Membuat objek turtle
t = turtle.Turtle()

# Menampilkan arah mata pencetak saat ini (default 0)
print(t.heading())

# Mengubah arah mata pencetak ke 90 derajat
t.setheading(90)

# Menampilkan arah mata pencetak saat ini (90)
print(t.heading())

# Mengubah arah mata pencetak ke 180 derajat
t.setheading(180)

# Menampilkan arah mata pencetak saat ini (180)
print(t.heading())



# Untuk menjelaskan heading() dan setheading() dalam Python,
# kita dapat menggunakan modul turtle.
# Modul turtle ini memungkinkan kita untuk membuat gambar sederhana
# dengan menggunakan perintah turtle yang sederhana.
# Berikut ini adalah contoh kode lengkap untuk menjelaskan heading() dan setheading():

# Perintah heading() digunakan untuk mengetahui arah mata pencetak saat ini,
# sedangkan setheading() digunakan untuk mengubah arah mata pencetak
# ke suatu nilai yang ditentukan. Dalam contoh di atas,
# kita mengubah arah mata pencetak dari 0 derajat ke 90 derajat,
# kemudian menampilkan arah mata pencetak saat ini menggunakan perintah heading()
# dan setelah itu mengubah arah mata pencetak menjadi 180 derajat.